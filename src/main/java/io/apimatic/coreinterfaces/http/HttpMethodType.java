package io.apimatic.coreinterfaces.http;

/**
 * HTTP methods enumeration.
 */
public enum HttpMethodType implements Method {
    /**
     * Get Http method
     */
    GET,
    /**
     * Post Http method
     */
    POST,
    /**
     * Put Http method
     */
    PUT,
    /**
     * Patch Http method
     */
    PATCH,
    /**
     * Delete Http method
     */
    DELETE,
    /**
     * Head Http method
     */
    HEAD
}


